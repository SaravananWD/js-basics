document.addEventListener("DOMContentLoaded", () => {
  displayMenu(menu);
  let buttonContainer = document.querySelector(".btn-container");
  buttonContainer.innerHTML = menu
    .reduce((categories, item) => {
      if (!categories.includes(item.category)) {
        categories.push(item.category);
      }
    }, "all")
    .map((category) => {
      return `<button class="btn-s filter-btn">${category}</button>`;
    });
});

function displayMenu(menuSelected) {
  let menuContainer = document.querySelector(".menu-container");
  menuContainer.innerHTML = menuSelected
    .map((item) => {
      return `<article class="menu-item"><img src="./img/${item.category}/${item.src}"
class="menu-item-img"><div class="menu-item-info"><p>${item.description}</p></div></article>`;
    })
    .join("");
}
