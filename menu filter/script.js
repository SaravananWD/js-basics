document.addEventListener("DOMContentLoaded", () => {
  displayMenuItems(menu);
  displayFilterButtons(menu);
});

function displayMenuItems(menuSelected) {
  let menuContainer = document.querySelector(".menu-container");
  menuContainer.innerHTML = menuSelected
    .map((item) => {
      return `<article class="menu-item"><img src="./img/${item.category}/${item.src}"
class="menu-item-img"><div class="menu-item-info"><p>${item.description}</p></div></article>`;
    })
    .join("");
}

function displayFilterButtons(menuSelected) {
  let buttonContainer = document.querySelector(".btn-container");

  buttonContainer.innerHTML = menu
    .reduce(
      (values, item) => {
        if (!values.includes(item.category)) {
          values.push(item.category);
        }
        return values;
      },
      ["all"]
    )
    .map((category) => {
      return `<button class="btn-s filter-btn" data-id="${category}">${category}</button>`;
    })
    .join("");

  // act on filter btn click

  let filterButtons = document.querySelectorAll(".filter-btn");

  filterButtons.forEach((btn) => {
    btn.addEventListener("click", (e) => {
      currentCategory = e.currentTarget.dataset.id;
      menuCategory = menu.filter((item) => {
        if (item.category === currentCategory) {
          return item;
        }
      });
      if (currentCategory === "all") {
        displayMenuItems(menu);
      } else {
        displayMenuItems(menuCategory);
      }
    });
  });
}
